fi

# Custom completion for package names
_spine_packages() {
    local packages
    packages=$(spine list-packages-for-completion 2>/dev/null || echo "")
    COMPREPLY=($(compgen -W "$packages" -- "${COMP_WORDS[COMP_CWORD]}"))
}

# Override completion for link, unlink, and remove commands
complete -F _spine_packages spine link
complete -F _spine_packages spine unlink
complete -F _spine_packages spine remove
